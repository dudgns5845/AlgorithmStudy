#점화식을 세워보자
# dp[1] = 1 ==> 1
# dp[2] = 1 1 / 2 ==> 2
# dp[3] = 1 1 1 / 1 2 / 2 1 / 3 ==> 4
# dp[4] = 1111/ 112/121/211 /22/ 13/31/ ==> 7
# dp[5] = 11111/1112/1121/1211/2111/122/212/221/113/131/311/32/23/ ==> 13
# 점화식 발견함 dp[i] = dp[i-1]+dp[i-2]+dp[i-3] (i > 4)

T = int(input())

dp = [0]*12

dp[1] = 1
dp[2] = 2
dp[3] = 4

for i in range(4,12):
    dp[i] = dp[i-1] + dp[i-2] + dp[i+3]

for _ in range(T):
    n = int(input())
    print(dp[n])
    
